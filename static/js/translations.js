// Get current language from localStorage or default to 'tk'
function getCurrentLanguage() {
    return localStorage.getItem('app_language') || 'tk';
}

// Set language
function setLanguage(lang) {
    localStorage.setItem('app_language', lang);
}

// Get translation
function t(key) {
    const lang = getCurrentLanguage();
    return TRANSLATIONS[lang][key] || TRANSLATIONS['tk'][key] || key;
}